                            <h1>Confi<span>dentiality</span></h1>
                            <p>Any manuscripts received for review must be treated as confidential
                                documents. They must not be shown to or discussed with others except as
                                authorized by the editor. The editor and any editorial staff must not
                                disclose any information about a submitted manuscript to anyone other than
                                the corresponding author, reviewers, potential reviewers, other editorial
                                advisers, and the publisher, as appropriate.
                            </p>
                        </div>
                    </div>

                    <div class="col-md-8 col-sm-6 col-xs-12">
                        <div class="aboutus-content" data-aos="fade-right" data-aos-duration="1000">

                            <h1>Standards of <span>Objectivity</span></h1>
                            <p>Reviews should be conducted objectively. Personal criticism of the author
                                is inappropriate. Referees should express their views clearly with
                                supporting arguments. </p>
                        </div>
                    </div>


                    <div class="col-md-8 col-sm-6 col-xs-12">
                        <div class="aboutus-content" data-aos="fade-right" data-aos-duration="1000">

                            <h1>Acknowledgement <span>of Sources</span></h1>
                            <p>Reviewers should identify relevant published work that has not been
                                cited by the authors. Any statement that an observation, derivation,
                                or argument had been previously reported should be accompanied by
                                the relevant citation. A reviewer should also call to the editor's
                                attention any substantial similarity or overlap between the
                                manuscript under consideration and any other published paper of
                                which they have personal knowledge.
                            </p>
                        </div>
                    </div>


                    <div class="col-md-8 col-sm-6 col-xs-12">
                        <div class="aboutus-content" data-aos="fade-right" data-aos-duration="1000">

                            <h1>Disclosure and <span>Conflict of Interest</span></h1>
                            <p>Privileged information or ideas obtained through peer review must
                                be kept confidential and not used for personal advantage.
                                Reviewers should not consider manuscripts in which they have
                                conflicts of interest resulting from competitive, collaborative,
                                or other relationships or connections with any of the authors,
                                companies, or institutions connected to the papers. <br> <br>At the point
                                of submission, policy requires that each author reveal any
                                financial interests or connections, direct or indirect, or other
                                situations that might raise the question of bias in the work
                                reported or the conclusions, implications, or opinions stated -
                                including pertinent commercial or other sources of funding for
                                the individual author(s) or for the associated department(s) or
                                organization(s), personal relationships, or direct academic
                                competition. If the manuscript is accepted, Conflict of Interest
                                information will be communicated in a published statement. <br><br>
                                Unpublished materials disclosed in a submitted manuscript must
                                not be used in an editor's own research without the express
                                written consent of the author.
                            </p>
                        </div>
